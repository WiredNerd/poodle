def addition(x, y):
    return x + y


def subtraction(x, y):
    return x - y


def multiplication(x, y):
    return x * y


def division(x, y):
    return x / y


def modulus(x, y):
    return x % y


def floor_division(x, y):
    return x // y


def exponentiation(x, y):
    return x**y


def left_shift(x, y):
    return x << y


def right_shift(x, y):
    return x >> y


def bit_or(x, y):
    return x | y


def bit_xor(x, y):
    return x ^ y


def bit_and(x, y):
    return x & y
